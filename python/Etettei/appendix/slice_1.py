import numpy as np

a = np.array([1, 3, 5, 7, 9, 11, 13])

print(a[2:5]) #スライス

'''
[5 7 9]
'''

print(a[:5]) #始点の省略

'''
[1 3 5 7 9]
'''

print(a[2:]) #終点の省略

'''
[ 5  7  9 11 13]
'''

print(a[2:5:2]) #刻み幅の指定

'''
[5 9]
'''

print(a[::-1]) #逆順

'''
[13 11  9  7  5  3  1]
'''

print(a[-3:0:-1]) #逆順

'''
[9 7 5 3]
'''