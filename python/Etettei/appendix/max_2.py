import numpy as np

a = np.array([[1, 3, 5, 7, 9, 11, 13],
            [2, 4, 6, 8, 10, 12, 14],
            [3, 5, 7, 9, 11, 13, 15],
            [2, 4, 6, 8, 10, 12, 14],
            [1, 3, 5, 7, 9, 11, 13]])

print(np.argmax(a)) #最大値をとるインデックス（1 次元 array に引き伸ばされている）

'''
20
'''

print(np.argmax(a, axis=0))

'''
[2 2 2 2 2 2 2]
'''

print(np.argmax(a, axis=1))

'''
[6 6 6 6 6]
'''
