import numpy as np

a = np.array([[1, 3, 5, 7, 9, 11, 13],
            [2, 4, 6, 8, 10, 12, 14],
            [3, 5, 7, 9, 11, 13, 15],
            [4, 6, 8, 10, 12, 14, 16],
            [5, 7, 9, 11, 13, 15, 17]])

print(a[2:4, :]) #スライス

'''
[[ 3  5  7  9 11 13 15]
 [ 4  6  8 10 12 14 16]]
'''

print(a[:, 2:4]) #スライス

'''
[[ 5  7]
 [ 6  8]
 [ 7  9]
 [ 8 10]
 [ 9 11]]
'''

print(a[2:4, 2:4]) #スライス

'''
[[ 7  9]
 [ 8 10]]
'''

print(a[:4:2, -2:1:-2]) #刻み幅の指定

'''
[[11  7]
 [13  9]]
'''
